#Programa que solicite al usuario los datos para calcular el área de un Triángulo (▲),
#finalmente mostrar el resultado en pantalla.

lado_triangulo1 = int(input('Escriba un lado del triangulo '))
lado_triangulo2 = int(input('Escriba el segundo lado del triangulo '))
lado_triangulo3 = int(input('Escriba el tercer lado del triangulo '))
resultado = (lado_triangulo1 * lado_triangulo2 * lado_triangulo3)
print ('El area del triangulo es', resultado)